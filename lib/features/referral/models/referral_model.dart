class ReferralModel {
  final String name;
  final String jobTitle;
  final bool isActive;
  final String date;
  final double bonusAmount;
  final String imageUrl;

  ReferralModel({
    required this.name,
    required this.jobTitle,
    required this.isActive,
    required this.date,
    required this.bonusAmount,
    required this.imageUrl,
  });
}
